#include "libft.h"

char	*ft_strstr(const char *haystack, const char *needle)
{
	int i;
	int j;

	i = 0;
	if (!(*needle))
		return ((char*)haystack);
	while (haystack[i])
	{
		j = 0;
		while (haystack[i] == needle[j] && needle[j])
		{
			i++;
			j++;
		}
		if (!(needle[j]))
			return ((char*)&haystack[i - j]);
		else if (i > 0)
			i -= j;
		i++;
	}
	return (NULL);
}
