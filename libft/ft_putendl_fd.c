#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;
	char	*p_s;

	if (s != NULL)
	{
		p_s = (char*)s;
		i = 0;
		while (p_s[i])
		{
			write(fd, &p_s[i], 1);
			i++;
		}
		write(fd, "\n", 1);
	}
}
