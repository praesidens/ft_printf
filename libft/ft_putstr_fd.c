#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int		i;
	char	*p_s;

	i = 0;
	if (s != NULL)
	{
		p_s = (char*)s;
		while (p_s[i])
		{
			write(fd, &p_s[i], 1);
			i++;
		}
	}
}
