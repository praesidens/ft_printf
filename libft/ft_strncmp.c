#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	p_s1;
	unsigned char	p_s2;
	size_t			i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && s1[i] && s2[i] && i < n - 1)
		i++;
	p_s1 = s1[i];
	p_s2 = s2[i];
	return (p_s1 - p_s2);
}
