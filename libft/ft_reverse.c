#include "libft.h"

void	ft_reverse(char *a)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = ft_strlen(a) - 1;
	while (i < j)
	{
		c = a[i];
		a[i] = a[j];
		a[j] = c;
		j--;
		i++;
	}
}
